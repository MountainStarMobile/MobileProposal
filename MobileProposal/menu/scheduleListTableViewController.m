- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    static NSString *scheduleCellIdentifier = @"scheduleCellIdentifier";
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:scheduleCellIdentifier];
    if (cell == nil)
    {
        cell = [[[UITableViewCell alloc] initWithStyle:UITableViewCellStyleSubtitle reuseIdentifier:scheduleCellIdentifier] autorelease];
        [cell setAccessoryType:UITableViewCellAccessoryDetailDisclosureButton];
    }
    [cell.textLabel setText:[NSString stringWithFormat:@"%i.",[indexPath row]]];
    [cell.detailTextLabel setText:[NSString stringWithFormat:@"行程管理-%i",[indexPath row]]];
    
    return cell;
}

